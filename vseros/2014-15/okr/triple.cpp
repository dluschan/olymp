#include <iostream>

using namespace std;

int main()
{
  int m;
  int t[3];
  cin >> t[0] >> t[1] >> t[2] >> m;
  int a[3] = {15, 10, 5};

  cout << min((m % a[0] == 0) ? t[0] : a[0] - (m % a[0]) + t[0], min((m % a[1] == 0) ? t[1] : a[1] - (m % a[1]) + t[1], (m % a[2] == 0) ? t[2] : a[2] - (m % a[2]) + t[2])) << endl;
  return 0;
}
